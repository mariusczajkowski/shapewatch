var langs = ['en', 'cn',];
var current_lang_index = 0;
var current_lang = langs[current_lang_index];

window.change_lang = function() {
	current_lang_index = ++current_lang_index % 2;
	current_lang = langs[current_lang_index];
	if (current_lang == 'cn'){
		$('.changeLang a img.cn-flag').hide();
		$('.changeLang a img.us-flag').fadeIn();			
	}else{
		$('.changeLang a img.us-flag').hide();
		$('.changeLang a img.cn-flag').fadeIn();		
	}
	translate();
}

function translate() {

	$("[data-translate]").each(function(){
		var key = $(this).data('translate');
		$(this).hide().html(dictionary[key][current_lang] || "N/A").fadeIn();		
	});
}

translate();